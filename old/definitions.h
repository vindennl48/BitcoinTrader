////////////////////////////////////////////////////////////////////////////////
inline std::string dbl_to_s(double num, int precision){
  std::string result;
  
  std::stringstream stream;
  stream << std::fixed << std::setprecision(precision) << num;
  result = stream.str();

  return result;
};



